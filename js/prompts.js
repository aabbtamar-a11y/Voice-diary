let el = null;
let intervalId = null;
let lastIndex = -1;

function pickPrompt() {
  let idx;
  do {
    idx = Math.floor(Math.random() * PROMPTS.length);
  } while (idx === lastIndex && PROMPTS.length > 1);
  lastIndex = idx;
  return PROMPTS[idx];
}

export function startPromptRotation(promptEl) {
  el = promptEl;
  el.textContent = pickPrompt();
  el.classList.remove('hidden');
  clearInterval(intervalId);
  intervalId = setInterval(() => {
    el.textContent = pickPrompt();
  }, ROTATE_MS);
}

export function stopPromptRotation() {
  clearInterval(intervalId);
  intervalId = null;
  if (el) el.classList.add('hidden');
}
